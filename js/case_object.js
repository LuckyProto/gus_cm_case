    var caseObj = {};
    var cases = [];
    $('*').each(function () {
        var caseItem = {};
        if($(this).attr('type') == 'radio'){
            if($(this).attr('checked') == 'checked'){
                if($(this).attr('itemid')){
                    caseItem.itemId   = $(this).attr('itemid');
                }else{
                    caseItem.itemId   = $(this).parent().next().attr('itemid');
                }

                if($(this).attr('optionId')){
                    caseItem.itemValue   = $(this).attr('optionId');
                    // caseItem.value_inputRadio = $(this).val();
                }else{
                    caseItem.itemValue = $(this).parent().next().val();
                }

                cases.push(caseItem)
            }
        }else if($(this).attr('type') == 'checkbox'){
            if($(this).attr('checked') == 'checked'){
                caseItem.itemId   = $(this).attr('itemid');
                caseItem.itemValue = [];
                caseItem.itemValue.push($(this).attr('optionId'))
                cases.push(caseItem)
            }
        }else if($(this).attr('type') == 'text'){
            if($(this).val()){
                caseItem.itemId   = $(this).attr('itemid');
                caseItem.itemValue = $(this).val();
                cases.push(caseItem)
            }
        }else if($(this).attr('type') == 'textarea'){
            if($(this).val()){
                caseItem.itemId   = $(this).attr('itemid');
                caseItem.itemValue = $(this).val();
                cases.push(caseItem)
            }
        }else if($(this).attr('type') == 'file'){
            if($(this).val()){
                caseItem.itemId   = $(this).attr('itemid');
                caseItem.itemValue = $(this).attr('value');
                cases.push(caseItem)
            }
        }
    })
    if(caseImg){
        cases = cases.concat(caseImg)
    }
    var re = unique(cases)

    //过滤重复的item
    caseObj.patient_case_id = caseAuditId;
    caseObj.items = re;
